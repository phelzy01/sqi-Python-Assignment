class my():
    def __init__(self,nu):
        self.nu = nu
    
    def SI(nu,mu,pu,):
        con = (nu * mu * pu )/(100)
        print(f"The simple interest is {con}Naira")
    
    def P(nu,mu,pu):
        pol = (nu * 100)/(mu*pu)
        print(f"The Principal amount is {pol}Naira")
    
    def R(nu,mu,pu):
        sol = (nu*100)/(mu*pu)
        print(f'The rate is {sol}%')

    def T(nu,mu,pu): 
        tol = (nu*100)/(mu*pu)
        print(f'The time taken is {tol}year(s)')    
        

